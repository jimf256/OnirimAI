#include "Card.h"

// -------------------------------------------------------------------------------------------------

Card::Card()
	: m_color(EColor::None)
	, m_type(ECardType::Door)
{
}

// -------------------------------------------------------------------------------------------------

Card::Card(EColor color, ECardType type)
	: m_color(color)
	, m_type(type)
{
}

// -------------------------------------------------------------------------------------------------

bool operator==(const Card& left, const Card& right)
{
	return (left.Color() == right.Color() && left.Type() == right.Type());
}

